class MouseDragHandler extends java.awt.event.MouseAdapter {
    @Override
    public void mousePressed(java.awt.event.MouseEvent e) {
        java.awt.Component c = (java.awt.Component) e.getSource();
        javax.swing.JList lc;
        if ((lc=GameUtilities.castToJList(c))==null) return;
        if (lc.getDragEnabled()) {
            lth.initSourceVariable(lc);
            lth.exportAsDrag(lc, e, javax.swing.TransferHandler.MOVE);
        }
    }
}  

