    private void prepareList(java.awt.Component c, ListTransferHandler lth) {
        if (c != null && (c instanceof  java.awt.Container)) {
            if (c instanceof javax.swing.JList) {
                javax.swing.JList jc = (javax.swing.JList) c;
                jc.setTransferHandler(lth);
                jc.addMouseListener(new MouseDragHandler());
            }
            if (c instanceof javax.swing.JFormattedTextField) {
                javax.swing.JFormattedTextField tf = (javax.swing.JFormattedTextField) c;
                tf.addFocusListener(new java.awt.event.FocusListener() {
                    @Override
                    public void focusGained(java.awt.event.FocusEvent e) {}
                    @Override
                    public void focusLost(java.awt.event.FocusEvent e) {
                        try {
                            JFormattedTextField tf = (JFormattedTextField) e.getSource();
                            MyController.temperatures.put(tf.getName(), 
                            Integer.parseInt(tf.getText().trim()));
                        } catch (NumberFormatException ne) {}
                    }
                });
            }
            java.awt.Container jc = (java.awt.Container) c;
            for (java.awt.Component cc: jc.getComponents())
                prepareList(cc, lth);
        }
    }
    
    
